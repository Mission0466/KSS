package com.example.KSS.dtos;

public class ProductDTO {
    private Integer weight;
    private Integer coffeeRatio;
    private Integer chicoryRatio;

    public Integer getWeight() {
        return weight;
    }

    public void setWeight(Integer weight) {
        this.weight = weight;
    }

    public Integer getCoffeeRatio() {
        return coffeeRatio;
    }

    public void setCoffeeRatio(Integer coffeeRatio) {
        this.coffeeRatio = coffeeRatio;
    }

    public Integer getChicoryRatio() {
        return chicoryRatio;
    }

    public void setChicoryRatio(Integer chicoryRatio) {
        this.chicoryRatio = chicoryRatio;
    }
}
